package lignesclaires.cmd;

/**
 * The Enum Verbosity represents the verbosity levels of a program.
 */
public enum Verbosity {

	/** The silent level. */
	SILENT,
	/** The quiet level. */
	QUIET,
	/** The normal level. */
	NORMAL,
	/** The verbose level. */
	VERBOSE,
	/** The very verbose level. */
	VERY_VERBOSE,
	/** The debug level. */
	DEBUG,
	/** The trace level. */
	TRACE;

}
